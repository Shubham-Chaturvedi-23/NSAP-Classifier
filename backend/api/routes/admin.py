# ─── Officer Activity ─────────────────────────────────────────
@router.get("/officers/activity")
def get_officer_activity(
    current_user: User    = Depends(require_admin),
    db:           Session = Depends(get_db),
):
    """
    GET /api/v1/admin/officers/activity

    Summary of all officers' decision activity.
    Shows total decisions, approvals, rejections and overrides
    for each officer — useful for monitoring officer performance.

    Returns:
        dict: List of officer activity summaries.
    """
    officers = db.query(User).filter(User.role == "officer").all()

    activity = []
    for officer in officers:
        decisions = db.query(Decision).filter(
            Decision.officer_id == officer.id
        ).all()

        total     = len(decisions)
        approved  = sum(1 for d in decisions if d.decision == "approved")
        rejected  = sum(1 for d in decisions if d.decision == "rejected")
        overrides = sum(1 for d in decisions if d.override_scheme)

        # Today's count
        today           = datetime.now().date()
        decided_today   = sum(
            1 for d in decisions
            if d.decided_at and d.decided_at.date() == today
        )

        activity.append({
            "officer_id":       officer.id,
            "officer_name":     officer.name,
            "officer_email":    officer.email,
            "total_decisions":  total,
            "approved":         approved,
            "rejected":         rejected,
            "overrides":        overrides,
            "decisions_today":  decided_today,
            "approval_rate":    round(
                (approved / total * 100) if total > 0 else 0, 1
            ),
        })

    # Sort by total decisions descending
    activity.sort(key=lambda x: x["total_decisions"], reverse=True)

    return {
        "total_officers": len(officers),
        "officers":       activity,
    }


# ─── All Users ────────────────────────────────────────────────
@router.get("/users")
def get_all_users(
    role_filter: Optional[str] = None,
    limit:       int           = 50,
    offset:      int           = 0,
    current_user: User         = Depends(require_admin),
    db:          Session       = Depends(get_db),
):
    """
    GET /api/v1/admin/users

    List all registered users with optional role filter.

    Args:
        role_filter (str): Filter by role (citizen/officer/admin).
        limit       (int): Max results (default 50).
        offset      (int): Pagination offset.

    Returns:
        dict: Paginated user list.
    """
    query = db.query(User)

    if role_filter:
        query = query.filter(User.role == role_filter)

    total = query.count()
    users = (
        query
        .order_by(User.created_at.desc())
        .offset(offset)
        .limit(limit)
        .all()
    )

    return {
        "total":  total,
        "limit":  limit,
        "offset": offset,
        "data": [
            {
                "id":         u.id,
                "name":       u.name,
                "email":      u.email,
                "phone":      u.phone,
                "role":       u.role,
                "state":      u.state,
                "is_active":  u.is_active,
                "created_at": u.created_at.isoformat(),
            }
            for u in users
        ],
    }